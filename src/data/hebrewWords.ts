export type Difficulty = keyof typeof words;

export function getRandomWord(difficulty: Difficulty, usedWords: string[]): string {
  const availableWords = words[difficulty].filter(
    (word) => !usedWords.includes(word)
  );

  if (availableWords.length === 0) {
    // Reset if all words used
    return words[difficulty][Math.floor(Math.random() * words[difficulty].length)];
  }

  return availableWords[Math.floor(Math.random() * availableWords.length)];
}

export function getAllWords(difficulty: Difficulty): string[] {
  return words[difficulty];
}
